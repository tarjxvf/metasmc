void __add_edge(struct genealogy *G, int pop, struct edge *e)
{
	struct population *ppop;
	struct list *eid_queue;
	struct list_head *l;
	int idx, *ptr;

	ppop = &G->pops[pop];
	idx = ppop->nedges++;
	ppop->eptrs[idx] = e;
#ifdef DEBUG
	fprintf(stderr, "%s: %d: idx=%d, maxedges=%d, nedges=%d, e=%x\n", __func__, __LINE__, idx, ppop->maxedges, ppop->nedges, e);
#endif

	e->idx = idx;
}

/* Add an edge to a population. */
void add_edge(struct genealogy *G, int pop, struct edge *e)
{
	tsindex_add(G->tr_xover, e);
	__add_edge(G, pop, e);
}

void __remove_edge(struct genealogy *G, int pop, struct edge *e)
{
	struct population *ppop;
	void *l;
	int *pidx, idx;

	ppop = &G->pops[pop];
	idx = e->idx;
	e = ppop->eptrs[idx];
	ppop->eptrs[idx] = ppop->eptrs[--(ppop->nedges)];
	ppop->eptrs[idx]->idx = idx;
	ppop->eptrs[ppop->nedges] = NULL;
}

void remove_edge(struct genealogy *G, int pop, struct edge *e)
{
	__remove_edge(G, pop, e);
	tsindex_clear(G->tr_xover, e);
	free_edge(G, e);
}

void insert_event_rb_join(struct genealogy *G, struct join_event *jev)
{
	struct rb_traverser tr;

	rb_t_find(&tr, G->evidx->idx->tree, (void *)jev);
	dn_clear(G->cfg->npop_all, G->evidx->dn);
	G->evidx->dn[jev->popi] = 1;
	G->evidx->dn[jev->popj] = -1;

	dn_add(G->cfg->npop_all, jev->sumdn, G->evidx->dn);
	evindex_propagate_add(tr.rb_height, tr.rb_stack, G->cfg->npop_all, G->evidx->dn);

	insert_event_join_increase(G, jev);
}

void insert_event_rb_splt(struct genealogy *G, struct splt_event *sev)
{
	struct rb_traverser tr;

	rb_t_find(&tr, G->evidx->idx->tree, (void *)sev);
	dn_clear(G->cfg->npop_all, G->evidx->dn);
	G->evidx->dn[sev->newpop] = 1;
	G->evidx->dn[sev->pop] = -1;

	dn_add(G->cfg->npop_all, sev->sumdn, G->evidx->dn);
	evindex_propagate_add(tr.rb_height, tr.rb_stack, G->cfg->npop_all, G->evidx->dn);

	insert_event_splt_increase(G, sev);
}

void remove_event_rb_join(struct genealogy *G, struct join_event *jev)
{
	struct rb_traverser tr;

#ifdef DEBUG
	fprintf(stderr, "Entering function %s, event=%x, type=%d, t=%.6f\n", __func__, jev, jev->type, jev->t);
#endif

	rb_t_find(&tr, G->evidx->idx->tree, (void *)jev);
	dn_clear(G->cfg->npop_all, G->evidx->dn);
	G->evidx->dn[jev->popi] = 1;
	G->evidx->dn[jev->popj] = -1;

	// Update tree values
	dn_sub(G->cfg->npop_all, jev->sumdn, G->evidx->dn);
	evindex_propagate_sub(tr.rb_height, tr.rb_stack, G->cfg->npop_all, G->evidx->dn);

	remove_event_join_decrease(G, jev);
}


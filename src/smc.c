/* Generate recombination point and modify genealogy */
double recombination(struct genealogy *G, double x)
{
	struct config *cfg;
	int pop, coalesced, i;	// coalesced: flag indicating whether floating lineage is absorbed
	struct list_head *l;
	struct event *ev;
	struct edge *e, *ef;	// e: edge where recombination event occur
	struct dummy_node *nf;
	double t, like, tdiff_below;

	struct edge *e_below_xover;	// For simulating behavior of macs
	struct xover_node *nxover;

	// Leave sequential mode and rebuild red-black tree index
	if(rbindex_isseq(G->evidx->idx))
		evindex_seq_off(G->evidx);

	cfg = G->cfg;
	/* Choose recombination point at random. */
	ev = rnd_select_point(G, &e, &pop, &t);

#ifdef DEBUG
	fprintf(stderr, "Recombination on %x, t=%.6f, pop=%d, range=[%x(%d, %.6f), %x(%d, %.6f)]\n", e, t, pop, e->bot, e->bot->type, e->bot->t, e->top, e->top->type, e->top->t);
	dump_edges(G);
	fprintf(stderr, "%d:", __LINE__);
	for(i = 0; i < cfg->npop + cfg->nsplt; i++)
		fprintf(stderr, ",G->pops[%d].n=%d, G->pops[%d].enabled=%d", i, G->pops[i].n, i, G->pops[i].enabled);
	fprintf(stderr, "\n");
	fprintf(stderr, "Next event: ev=%x, type=%d, t=%6f\n", ev, ev->type, ev->t);
#endif

	/* Generate recombination node */
	nxover = (struct xover_node *)alloc_node(G, NODE_XOVER, pop, t);

	/* Insert recombination node into target edge. */
	e_below_xover = alloc_edge(G, (struct node *)nxover, e->bot);
//	add_edge(G, pop, e_below_xover);
	e->bot = (struct node *)nxover;
	nxover->in = e;
	nxover->out = e_below_xover;

	/* Remove recombinant lineage from the genealogy. */
	nf = (struct dummy_node *)alloc_node(G, NODE_FLOAT, pop, INFINITY);
	ef = alloc_edge(G, (struct node *)nf, e->bot);
//	ef->ub = e_below_xover->ub = e->ub;
//	e->ub = x;
	nf->out = ef;
	nxover->in_new = ef;
	tdiff_below = e_below_xover->top->t - e_below_xover->bot->t;
//	tsindex_update(G->tr_xover, e, -tdiff_below);

	/* Iterate until floating lineage is absorbed. */
	coalesced = 0;
	like = 0;
	do{
		double at, mg, dmig, sublike, totalprob;
		struct list_head *el;

//	n_abs_time_xover++;

		totalprob = G->pops[pop].n;
//		at = ca_time(G, 1, pop, t);
		at = abs_time(G, 1, pop, t);

		dmig = G->pops[pop].mrate[pop] * 1;
		if(dmig > 0){
			totalprob += dmig;
			mg = dexp(dmig);
//			mg = -log(dunif01()) / dmig;

		}else{
			mg = INFINITY;
		}

//		ev = (struct event *)GET_OBJ(evl);
		ev = evindex_s_get(G->evidx);
		if(isinf(at) && isinf(mg) && isinf(ev->t)){
			like = -INFINITY;
			fprintf(stderr, "%d: Infinite time to next event\n", __LINE__);
			exit(-1);

		}else if(t + at < ev->t || t + mg < ev->t){
			struct event *evnew;

//	struct timespec beg, end;
//	int nsec;

//	n_abs_xover++;
//	clock_gettime(CLOCK_MONOTONIC, &beg);

			if(at < mg){		//Next event is absorption
				struct edge *e2;	// e2: Edge where new absorption event occur, e_old: the old branch from xover node
				struct migr_node *n;
				double tmrca_old;

				tmrca_old = G->localMRCA->t;
				sublike = -totalprob * at;
				t += at;
				e2 = choose_tedge(G, pop, t);

#ifdef DEBUG
				fprintf(stderr, "%d: Absorb floating lineage %x to %x, e=%x\n", __LINE__, ef, e2, e);
#endif

				if(e2 == e){	// Floating lineage is absorbed to the same lineage (loop in ARG)
					struct edge *e_old, *e_below, *e_new;

					e_old = nxover->in;
					e_new = nxover->in_new;
					e_below = nxover->out;

					free_node(G, ef->top);
					ef->top = e->top;
					if(e->top->type == NODE_COAL){
						ef->itop = e->itop;
						AS_COAL_NODE(e->top)->out[e->itop] = ef;

					}else{
						AS_MIGR_NODE(e->top)->out = ef;
					}

					e_new->bot = e_below->bot;
					e_below->bot->in = e_new;

//					// e_old->top is the new coalescent node which has to be removed. In this case, e2 must be in local genealogy, so tsindex must be updateda
					e->bot = e_below->bot;
					remove_edge(G, e->bot->pop, e);

					if(ef == e_new){
						add_edge(G, ef->bot->pop, ef);

					}else{
						add_edge(G, ef->bot->pop, ef);
						tsindex_update(G->tr_xover, e_new, tdiff_below);
					}

					free_node(G, (struct node *)nxover);
					free_edge(G, e_below);

				}else{
					struct edge *e_new, *e_below;

//					tsindex_update(G->tr_xover, e, -tdiff_below);

					e_below = nxover->out;
					e->bot = e_below->bot;
					evnew = __absorption_r(G, e2, ef, pop, t);
					insert_event_rb(G, evnew);

					e_new = AS_COAL_NODE(e2->bot)->out[0];
					e_new->bot->in = e_new;
					erase_dangling2(G, nxover->in);

					e_new = nxover->in_new;

					tsindex_update(G->tr_xover, e_new, tdiff_below);
					e_new->bot = e_below->bot;
					e_new->bot->in = e_new;

//					remove_edge(G, e_below->bot->pop, e_below);
					free_edge(G, e_below);
					free_node(G, (struct node *)nxover);
				}

				coalesced = 1;
//	clock_gettime(CLOCK_MONOTONIC, &end);
//	nsec = (end.tv_sec - beg.tv_sec) * MAXNSEC + (end.tv_nsec - beg.tv_nsec);
//	t_abs_xover += nsec;

			}else{	// Next event is migration
				struct migr_node *nd;
#ifdef DEBUG
				fprintf(stderr, "%d: Next event is migration\n", __LINE__);
#endif
				sublike = log(dmig) - totalprob * mg;
				t += mg;
				nd = __migration(G, ef, pop, t);
				evnew = (struct event *)nd->ev;
				insert_event_rb(G, evnew);
				pop = ((struct migr_event *)evnew)->spop;
			}

			like += sublike;

		}else{
//ndiscard_xover++;
			like -= totalprob * (ev->t - t);
			t = ev->t;

			if(ev->t == G->localMRCA->t){
				struct edge_set *F;
				struct edge *edum;
				struct node *ndum, *n;
				double troot_old;
				struct edge_set *trunk;

				e->bot = nxover->out->bot;
//				tsindex_update(G->tr_xover, e, -tdiff_below);
				troot_old = G->root->t;

				trunk = malloc(sizeof(struct edge_set) * cfg->npop_all);
				F = malloc(sizeof(struct edge_set) * cfg->npop_all);
				for(i = 0; i < cfg->npop_all; i++){
					edge_set_init(&trunk[i], 1);
					edge_set_init(&F[i], 2);
//					G->pops[G->localMRCA->pop].n = 0;
				}

				edge_set_add(&trunk[G->localMRCA->pop], G->localMRCA->in);
				G->pops[G->localMRCA->pop].n = 1;
				edge_set_add(&F[ef->bot->pop], ef);

//				G->root = G->localMRCA = NULL;	// localMRCA is cancelled because new local MRCA must be above current root

//				e->bot = nxover->out->bot;
				G->t = t;
				evindex_s_forward(G->evidx);
				like += merge_floating(G, trunk, F);

				{
					struct edge *e_new, *e_below, *e_prev_in;

					erase_dangling2(G, nxover->in);

					e_new = nxover->in_new;
					e_below = nxover->out;
					tsindex_update(G->tr_xover, e_new, tdiff_below);
					e_new->bot = e_below->bot;
					e_new->bot->in = e_new;
//					remove_edge(G, e_below->bot->pop, e_below);
					free_edge(G, e_below);
					free_node(G, (struct node *)nxover);
				}

				for(i = 0; i < cfg->npop_all; i++){
					edge_set_destroy(&trunk[i]);
					edge_set_destroy(&F[i]);
				}
				free(F);
				free(trunk);
				coalesced = 1;

			}else if(ev->type == EVENT_COAL){
				G->pops[((struct coal_event *)ev)->pop].n--;

			}else if(ev->type == EVENT_MIGR){
				G->pops[((struct migr_event *)ev)->dpop].n--;
				G->pops[((struct migr_event *)ev)->spop].n++;

			}else{
				update_demography(G, ev);

				if(ev->type == EVENT_JOIN){
					struct join_event *jev;
					struct migr_node *nd;
					struct edge *etmp;

					jev = (struct join_event *)ev;

					if(jev->popj == pop){
						/* Force the floating lineage moving from population j to population i */
						nd = (struct migr_node *)do_migrate(G, ef, jev->popj, jev->popi, t);
						nd->ev = (struct migr_event *)ev;
						list_append(&jev->ndls, nd);

//						G->evlist->n++;
						pop = jev->popi;
						insert_event_rb_join(G, jev);
					}

				}else if(ev->type == EVENT_SPLT){
					struct splt_event *sev;
					struct migr_node *nd;
					struct edge *etmp;
					double u;

					sev = (struct splt_event *)ev;

					if(sev->pop == pop){
						u = dunif01();
						if(u >= sev->prop){
							struct migr_node *nd;

							/* Move the floating lineage to new population */
							nd = (struct migr_node *)do_migrate(G, ef, sev->pop, sev->newpop, t);
							nd->ev = (struct migr_event *)ev;
							list_append(&sev->ndls, nd);
							pop = sev->newpop;
							insert_event_rb_splt(G, sev);
						}
					}

				}
			}

			evindex_s_forward(G->evidx);
//			if(ev->type == EVENT_DUMY || ev->type == EVENT_DXVR)
//				remove_event_rb(G, ev);

		}
	}while(!coalesced);

	return like;
}

double merge_floating(struct genealogy *G, struct edge_set *trunk, struct edge_set *F)
{
	struct config *cfg;
	struct event *ev, *evnew;
	struct node *last;
	struct list_head *f;
	int pop, uvpop, zpop, sumnF, i;
	double t, uv, minuv, z, minz;
	double like;

	cfg = G->cfg;
#ifdef DEBUG
	fprintf(stderr, "Entering function %s\n", __func__);
#endif
	t = G->t;
	sumnF = 0;
	for(pop = 0; pop < cfg->npop_all; pop++){
		sumnF += F[pop].n;
	}

	like = 0;
	/* If trunk genealogy is not empty, loop until all floating lineages are absorbed.
	   Otherwise, loop until only one floating lineage remained. */
	while((G->root != NULL && sumnF > 0) || (G->root == NULL && sumnF > 1)){
		double totalprob, sublike, rmig;

		totalprob = rmig = 0;
		minuv = minz = INFINITY;
		uvpop = zpop = 0;
		for(pop = 0; pop < cfg->npop + cfg->nsplt; pop++){
			if(F[pop].n > 0){
//n_abs_time_merge++;
				uv = ca_time(G, F[pop].n, pop, t);
#ifdef DEBUG
				fprintf(stderr, "%d: ca_time=%.10f, nF[%d]=%d, G->pops[%d].n=%d\n", __LINE__, uv, pop, F[pop].n, pop, G->pops[pop].n);
#endif
				totalprob += F[pop].n * G->pops[pop].n + F[pop].n * (F[pop].n - 1);

			}else{
				uv = INFINITY;
			}

			if(uv < minuv){
				minuv = uv;
				uvpop = pop;
			}
		}

		rmig = 0;
		for(pop = 0; pop < cfg->npop + cfg->nsplt; pop++){
			rmig += G->pops[pop].mrate[pop] * F[pop].n;
		}

		if(rmig > 0){
			minz = dexp(rmig);
			totalprob += rmig;

		}else{
			minz = INFINITY;
		}

		ev = evindex_s_get(G->evidx);
		if(isinf(minuv) && isinf(minz) && isinf(ev->t)){
			like = -INFINITY;
			fprintf(stderr, "%d: Infinite time to next event\n", __LINE__);
			exit(-1);
//			return -1;
		}
#ifdef DEBUG
		fprintf(stderr, "%d:", __LINE__);
		for(i = 0; i < cfg->npop + cfg->nsplt; i++)
			fprintf(stderr, ",G->pops[%d].n=%d", i, G->pops[i].n);
		fprintf(stderr, "\n");

		fprintf(stderr, "%d: Next event: ev=%x, type=%d, t=%6f\n", __LINE__, ev, ev->type, ev->t);
#endif

		if(t + minuv < ev->t || t + minz < ev->t){
			struct edge *e;
			int c, i;

			if(minuv < minz){	// Coalescent or Absorption
				double pabs, C, rate1, rate2, tdiff;

				// Calculate probability of absorption event
				tdiff = ev->t - t;
				rate1 = 2 * G->pops[uvpop].n * F[uvpop].n;
				rate2 = F[uvpop].n * (F[uvpop].n - 1);
				pabs = rate1 / (rate1 + rate2);

				t += minuv;
				if(dunif01() < pabs){	//Absorption
					c = dunif(F[uvpop].n);
					e = edge_set_remove(&F[uvpop], c);

					sublike = -totalprob * minuv;

					last = (struct node *)absorption(G, trunk, e, uvpop, t);
					evnew = last->ev;

				}else{	// Coalescent
					struct coal_node *nd;
					sublike = log(2) - totalprob * minuv;
					nd = coalescent(G, F, uvpop, t);
					last = (struct node *)nd;
					evnew = (struct event *)nd->ev;
				}
				sumnF--;

			}else{	// Migration
				struct migr_node *nd;
				struct edge *em;
				double sum, x, inc;
				int c;

				t += minz;
				x = rmig * dunif01();

				sum = 0;
				for(zpop = 0; zpop < cfg->npop_all; zpop++){
					inc = G->pops[zpop].mrate[zpop] * F[zpop].n;
					if(x < sum + inc)
						break;
					else
						sum += inc;
				}

				c = (int)((x - sum) / G->pops[zpop].mrate[zpop]);

finish_selection:

				// Find the lineage to be migrated
				em = edge_set_remove(&F[zpop], c);

				/* Perform migration */
				nd = __migration(G, em, zpop, t);
				last = (struct node *)nd;
				evnew = (struct event *)nd->ev;
				edge_set_add(&F[((struct migr_event *)evnew)->spop], em);
//				fprintf(stdout, "nmigr=%d, dpop=%d, c=%d, ev->dpop=%d, ev->spop=%d\n", nmigr, dpop, c, ev->dpop, ev->spop);

				sublike = log(G->pops[zpop].mrate[zpop]) - totalprob * minz;
			}

			like += sublike;
			insert_event(G, evnew);

		}else{
//ndiscard_merge++;
			like -= totalprob * (ev->t - t);
			t = ev->t;
			evindex_s_forward(G->evidx);

			if(ev->type == EVENT_COAL){
				struct coal_event *cev;
				int erase = 0;

				cev = (struct coal_event *)ev;
				edge_set_remove(&trunk[cev->nd->pop], cev->nd->out[0]->trunk_id);
				edge_set_remove(&trunk[cev->nd->pop], cev->nd->out[1]->trunk_id);

//				if(cev->nd->out[0]->ub < G->lb){
//					free_node(G, cev->nd->out[0]->bot);
//					free_edge(G, cev->nd->out[0]);
//					erase = 1;

//				}else{
//					add_edge(G, cev->nd->out[0]->pop, cev->nd->out[0]);
//				}

//				if(cev->nd->out[1]->ub < G->lb){
//					free_node(G, cev->nd->out[1]->bot);
//					free_edge(G, cev->nd->out[1]);
//					erase = 1;
//				}

//				if(erase)
//					remove_event(G, ev);

				edge_set_add(&trunk[cev->nd->pop], cev->nd->in);
				G->pops[((struct coal_event *)ev)->pop].n--;

			}else if(ev->type == EVENT_MIGR){
				struct migr_event *mev;

				mev = (struct migr_event *)ev;
				edge_set_remove(&trunk[mev->nd->out->bot->pop], mev->nd->out->trunk_id);

//				if(mev->nd->out->ub <= G->lb){
//					free_node(G, mev->nd->out->bot);
//					free_edge(G, mev->nd->out);
//					remove_event(G, ev);
//				}

				edge_set_add(&trunk[mev->nd->pop], mev->nd->in);
				G->pops[((struct migr_event *)ev)->dpop].n--;
				G->pops[((struct migr_event *)ev)->spop].n++;

			}else{
				update_demography(G, ev);

				if(ev->type == EVENT_JOIN){
					struct join_event *jev;
					struct migr_node *nd;
					struct list_head *l;
					struct edge *e;
					int i;

					jev = (struct join_event *)ev;
					/* Move edges of trunk genealogy. */
					l = jev->ndls.front;
					while(l){
						nd = (struct migr_node *)GET_OBJ(l);
						edge_set_remove(&trunk[nd->out->bot->pop], nd->out->trunk_id);

//						if(nd->out->ub <= G->lb){
//							free_node(G, nd->out->bot);
//							free_edge(G, nd->out);
//							remove_event(G, ev);
//						}

						edge_set_add(&trunk[nd->pop], nd->in);
						l = l->next;
					}

					/* Move all new lineages in population j to population i. */
					for(i = 0; i < F[jev->popj].n; i++){
						e = edge_set_get(&F[jev->popj], i);
						nd = (struct migr_node *)do_migrate(G, e, jev->popj, jev->popi, t);
						edge_set_add(&F[jev->popi], e);
						nd->ev = (struct migr_event *)ev;
						list_append(&jev->ndls, nd);
						insert_event_join(G, ev);
					}

					edge_set_clear(&F[jev->popj]);

				}else if(ev->type == EVENT_SPLT){
					struct splt_event *sev;
					struct migr_node *nd;
					struct list_head *l;
					struct edge *e;
					int i, nfrom;

					sev = (struct splt_event *)ev;
					/* Move edges of trunk genealogy. */
					l = sev->ndls.front;
					while(l){
						nd = (struct migr_node *)GET_OBJ(l);
						edge_set_remove(&trunk[nd->out->bot->pop], nd->out->trunk_id);

//						if(nd->out->ub <= G->lb){
//							free_node(G, nd->out->bot);
//							free_edge(G, nd->out);
//							remove_event(G, ev);
//						}

						edge_set_add(&trunk[nd->pop], nd->in);
						l = l->next;
					}

					/* Move new lineages in population old population to new population with probability sev->prop. */
					i = 0;
					while(i < F[sev->pop].n){
						struct edge *e2;
						double u;

						u = dunif01();
						if(u >= sev->prop){
							e = edge_set_remove(&F[sev->pop], i);
							nd = (struct migr_node *)do_migrate(G, e, sev->pop, sev->newpop, t);
							edge_set_add(&F[sev->newpop], e);
							nd->ev = (struct migr_event *)ev;
							list_append(&sev->ndls, nd);
							insert_event_splt(G, ev);

						}else{
							i++;
						}
					}

				}else if(ev->type == EVENT_DUMY || ev->type == EVENT_DXVR){
					struct edge *edum;
					struct node *ndum, *n;

#ifdef DEBUG
					if(ev->type == EVENT_DUMY){
						fprintf(stderr, "%s: %d: DUMY event is found at time %.6f\n", __func__, __LINE__, ev->t);

					}else{
						fprintf(stderr, "%s: %d: DXVR event is found at time %.6f\n", __func__, __LINE__, ev->t);
					}
#endif
					sumnF++;

					// Move edges in dummy edge list to in-tree edge list until t (Note that edges in dummy edge list are ordered by time)
					edum = G->localMRCA->in;
					ndum = edum->top;
					while(ndum->t < ev->t){
#ifdef DEBUG
						fprintf(stderr, "%s: %d: ", __func__, __LINE__);
#endif
						if(!tsindex_isin(G->tr_xover, edum))
							tsindex_add(G->tr_xover, edum);
						edum = ndum->in;
						ndum = edum->top;
					}

					for(i = 0; i < cfg->npop_all; i++)
						edge_set_clear(&trunk[i]);

					// Remove remaining edges in dummy edge list
					if(ev->type == EVENT_DXVR){
						/* Remove dangling edges above dummy recombination event from red-black tree. */
			//			edum->bot->in = NULL;
						erase_dummy_path(G, edum);

					}else{
						G->pops[G->root->pop].n = 0;
						ndum->ev = NULL;

						// Create new floating lineage
						ndum->type = NODE_FLOAT;
						ndum->t = INFINITY;
						ndum->in = NULL;
					}
					__remove_edge(G, edum->bot->pop, edum);
					edge_set_add(&F[edum->bot->pop], edum);

					G->root = G->localMRCA = NULL;
					remove_event(G, ev);
				}
			}
		}

	}

	if(G->localMRCA == NULL){	// In this case, last event must be coalescent between two floating lineages
		struct node *ndum;
		struct edge *e;

		for(i = 0; i < cfg->npop; i++)
			if(F[i].n > 0)
				break;
		e = edge_set_remove(&F[i], 0);
		e->top->type = NODE_DUMMY;
		e->top->t = e->bot->t;
		e->top->pop = e->bot->pop;
		e->top->in = NULL;
		__add_edge(G, e->bot->pop, e);
//		if(G->troot > e->top->t){
//			e->top->t = G->troot; // Change on 2018/05/23: This leads to missing of migration by population join/split.

//		}else{
			G->troot = e->top->t;
//		}

		G->root = e->top;
		G->localMRCA = e->bot;

		evnew = alloc_event(G->cfg, EVENT_DUMY, e->top->t);
		// Change on 2018/05/23: This is necessary because the next event may be demographic events before G->troot (old TMRCA).
		while((evindex_s_get(G->evidx))->t < evnew->t)
			evindex_s_forward(G->evidx);
		insert_event(G, evnew);
		e->top->ev = evnew;

	}else if(t > G->localMRCA->t){
		struct edge *edum;
		struct node *ndum;

		edum = G->localMRCA->in;
		ndum = edum->top;
		while(ndum->t < t){
			if(!tsindex_isin(G->tr_xover, edum))
				tsindex_add(G->tr_xover, edum);
			edum = ndum->in;
			ndum = edum->top;
		}

		G->localMRCA = last;
	}

#ifdef DEBUG
	fprintf(stderr, "%d:", __LINE__);
	for(i = 0; i < cfg->npop + cfg->nsplt; i++)
		fprintf(stderr, ",G->pops[%d].n=%d, G->pops[%d].enabled=%d", i, G->pops[i].n, i, G->pops[i].enabled);
	fprintf(stderr, "\n");

	/* Print current population size. */
	fprintf(stderr, "%d: Current population size:[", __LINE__);
	for(i = 0; i < cfg->npop + cfg->nsplt; i++)
		fprintf(stderr, "%.6f,", G->pops[i].size);
	fprintf(stderr, "]\n");

	/* Print current growth rate. */
	fprintf(stderr, "%d: Current growth rate:[", __LINE__);
	for(i = 0; i < cfg->npop + cfg->nsplt; i++)
		fprintf(stderr, "%.6f,", G->pops[i].grate);
	fprintf(stderr, "]\n");

	/* Print current migration matrix. */
	fprintf(stderr, "Current migration matrix:[\n");
	for(i = 0; i < cfg->npop + cfg->nsplt; i++){
		int j;
		for(j = 0; j < cfg->npop + cfg->nsplt; j++)
			fprintf(stderr, "%.6f,", G->pops[i].mrate[j]);
		fprintf(stderr, "\n");
	}
	fprintf(stderr, "]\n");

	dump_edges(G);

	fprintf(stderr, "Finishing function %s\n\n", __func__);
#endif
	return like;
//	return 0;
}

void destroy_tree(struct genealogy *G, struct node *nd)
{
	if(nd->type == NODE_COAL){
		struct coal_node *n;

		n = (struct coal_node *)nd;
		remove_event(G, (struct event *)n->ev);
		destroy_tree(G, n->out[0]->bot);
		destroy_tree(G, n->out[1]->bot);
		free_edge(G, n->out[0]);
		free_edge(G, n->out[1]);

	}else if(nd->type == NODE_MIGR){
		struct migr_node *n;

		n = (struct migr_node *)nd;
		remove_event_josp(G, (struct event *)n->ev);
		destroy_tree(G, n->out->bot);
		free_edge(G, n->out);

	}else if(nd->type == NODE_FLOAT || nd->type == NODE_DUMMY){
		struct dummy_node *n;

		n = (struct dummy_node *)nd;
		remove_event(G, (struct event *)n->ev);
		destroy_tree(G, n->out->bot);
		free_edge(G, n->out);
	}

	free_node(G, nd);
}

void destroy_pop(struct genealogy *G, struct population *p)
{
	struct list_head *l;
	int i;

	free(p->mrate);
	free(p->eptrs);

	while(p->idx_queue.front){
		l = __list_pop(&p->idx_queue);
		free(l);
	}

	while(p->id_list.front){
		l = __list_pop(&p->id_list);
		free(l);
	}
}

void clear_genealogy(struct genealogy *G)
{
	struct config *cfg;
	struct list_head *l;
	struct list *eq;
	struct event *ev0, *ev;
	int pop, i;

	cfg = G->cfg;
	if(G->root){
		destroy_tree(G, G->root);
		G->root = G->localMRCA = NULL;
	}
	cache_clear(G->cfg->edge_cache);

	cache_clear(G->cfg->node_cache[NODE_COAL]);
	cache_clear(G->cfg->node_cache[NODE_MIGR]);
	cache_clear(G->cfg->node_cache[NODE_XOVER]);
	cache_clear(G->cfg->node_cache[NODE_SAM]);

	cache_clear(G->cfg->event_cache[EVENT_COAL]);
	cache_clear(G->cfg->event_cache[EVENT_MIGR]);

	G->total = 0;
	G->edgeid = 0;

	// Detach right sentinel from event index
	l = __list_pop(&G->evidx->idx->ls);
	ev0 = (struct event *)GET_OBJ(l);
	dn_clear(cfg->npop_all, ev0->dn);

	// Detach left sentinel from event index
	l = G->evidx->idx->ls.front;
	__list_remove(&G->evidx->idx->ls, l);
	ev0 = (struct event *)GET_OBJ(l);
	dn_clear(cfg->npop_all, ev0->dn);

	if(G->ev_dxvr){
		remove_event(G, G->ev_dxvr);
		G->ev_dxvr = NULL;
	}

	// Clear event list
	while(G->evidx->idx->ls.front){
		l = __list_pop(&G->evidx->idx->ls);
		ev = (struct event *)GET_OBJ(l);
		if(ev->type == EVENT_DUMY || ev->type == EVENT_DXVR)
			free(l);
	}

	list_init(&G->n_list);

	tsindex_reset(G->tr_xover);
//	list_init(&G->e_list);

	for(pop = 0; pop < cfg->npop + cfg->nsplt; pop++){
		eq = &G->pops[pop].idx_queue;
		while(eq->front){
			l = __list_pop(eq);
			__list_append(&G->pops[pop].id_list, l);
//			free(l);
		}
//		list_init(&G->pops[pop].idx_queue);
/*		for(i = 0; i < G->pops[pop].nedges; i++){
			if(G->pops[pop].eptrs[i])
				free_edge(G, G->pops[pop].eptrs[i]);
		}*/
		memset(G->pops[pop].eptrs, 0, sizeof(struct edge *) * G->pops[pop].maxedges);
		G->pops[pop].nedges = 0;
		G->pops[pop].nsam = G->pops[pop].n = 0;
	}

	evindex_reset(G, G->evidx);
}

struct genealogy *alloc_genealogy(struct config *cfg, struct profile *prof)
{
	int pop, npop, nsplt, i, npop_all;
	struct genealogy *G;
	struct list_head *l;

	npop = cfg->npop;
	nsplt = cfg->nsplt;
	npop_all = npop + nsplt;

	G = malloc(sizeof(struct genealogy));
	memset(G, 0, sizeof(struct genealogy));

	G->cfg = cfg;
	cfg->node_cache[NODE_COAL] = cache_create(sizeof(struct list_head) + sizeof(struct coal_node), cfg->maxfrag * 4);
	cfg->node_cache[NODE_MIGR] = cache_create(sizeof(struct list_head) + sizeof(struct migr_node), cfg->maxfrag * 4);
	cfg->node_cache[NODE_XOVER] = cache_create(sizeof(struct list_head) + sizeof(struct xover_node), cfg->maxfrag * 4);
	cfg->node_cache[NODE_SAM] = cache_create(sizeof(struct list_head) + sizeof(struct sam_node), cfg->maxfrag * 4);
	cfg->node_cache[NODE_FLOAT] = cfg->node_cache[NODE_MIGR];
	cfg->node_cache[NODE_DUMMY] = cfg->node_cache[NODE_FLOAT];

	cfg->edge_cache = cache_create(sizeof(struct list_head) + sizeof(struct edge), cfg->maxfrag * 4);
	cfg->frag_cache = cache_create(sizeof(struct list_head) + sizeof(struct frag *), cfg->maxfrag);

	cfg->event_cache[EVENT_COAL] = cache_create(sizeof(struct list_head) + sizeof(struct coal_event) + sizeof(int) * 2 * npop_all, cfg->maxfrag * 4);
	cfg->event_cache[EVENT_MIGR] = cache_create(sizeof(struct list_head) + sizeof(struct migr_event) + sizeof(int) * 2 * npop_all, cfg->maxfrag * 4);

	G->pops = malloc(sizeof(struct population) * (npop + nsplt));
	memset(G->pops, 0, sizeof(struct population) * (npop + nsplt));
	for(pop = 0; pop < npop + nsplt; pop++){
		G->pops[pop].mrate = malloc(sizeof(double) * (npop + nsplt));
		memset(G->pops[pop].mrate, 0, sizeof(double) * (npop + nsplt));

		if(pop > npop){
			G->pops[pop].enabled = 0;

		}else{
			G->pops[pop].enabled = 1;
		}
		 
		list_init(&G->pops[pop].idx_queue);
		list_init(&G->pops[pop].id_list);
//		G->pops[pop].eptrs = NULL;
//		G->pops[pop].maxedges = G->pops[pop].nedges = 0;
		G->pops[pop].maxedges = prof->nfrag * 3;
		G->pops[pop].eptrs = malloc(sizeof(struct edge *) * G->pops[pop].maxedges);
		G->pops[pop].nedges = 0;
	}
	list_init(&G->n_list);
//	list_init(&G->e_list);
//	G->evlist = &cfg->evlist;
	G->evidx = evindex_create(G, G->cfg);
	evindex_seq_on(G->evidx);
	evindex_s_rewind(G->evidx);
	for(i = 0; i < cfg->ndevents; i++)
		evindex_s_insert(G->evidx, cfg->devents[i]);

	/* Set up mutation model of populations that exist at time 0. */
	for(pop = 0; pop < npop; pop++){
		G->pops[pop].mmut = cfg->mmut[pop];
	}

	/* Set up mutation model of populations generated by SPLT event. */
//	l = G->evlist->front;
	evindex_s_rewind(G->evidx);
	while(!evindex_s_end(G->evidx)){
		struct event *ev;
//		ev = (struct event *)GET_OBJ(l);
		ev = evindex_s_get(G->evidx);
		if(ev->type == EVENT_SPLT){
			struct splt_event *sev;

			sev = (struct splt_event *)ev;
			if(cfg->mmut[sev->newpop] == NULL){	// If user does not specify mutation model of a population that created by SPLT event, then it inherits mutation model of original population
				G->pops[sev->newpop].mmut = cfg->mmut[sev->pop];

			}else{
				G->pops[sev->newpop].mmut = cfg->mmut[sev->newpop];
			}
		}
		evindex_s_forward(G->evidx);
	}

	G->tr_xover = tsindex_alloc(cfg->maxfrag * 2 * 2);

	return G;
}

void destroy_genealogy(struct genealogy *G)
{
	struct list_head *l;
	struct config *cfg;
	struct event *ev;
	int pop;

	tsindex_free(G->tr_xover);
	cfg = G->cfg;
#ifdef DEBUG
	fprintf(stderr, "Entering function %s\n", __func__);
#endif
	if(G->root){
#ifdef DEBUG
		fprintf(stderr, "root=%x\n", G->root);
#endif
//		free_node(G, G->root->in->top);
//		free_edge(G, G->root->in);
		destroy_tree(G, G->root);
	}

	for(pop = 0; pop < cfg->npop + cfg->nsplt; pop++)
		destroy_pop(G, &G->pops[pop]);
	free(G->pops);

	// Detach right sentinel from event index
	l = __list_pop(&G->evidx->idx->ls);

	// Detach left sentinel from event index
	l = G->evidx->idx->ls.front;
	__list_remove(&G->evidx->idx->ls, l);

	while(G->evidx->idx->ls.front){
		l = __list_pop(&G->evidx->idx->ls);
		ev = (struct event *)GET_OBJ(l);
		if(ev->type == EVENT_DUMY || ev->type == EVENT_DXVR)
			free(l);
	}

	evindex_destroy(G, G->evidx);

	cache_destroy(G->cfg->node_cache[NODE_COAL]);
	cache_destroy(G->cfg->node_cache[NODE_MIGR]);
	cache_destroy(G->cfg->node_cache[NODE_XOVER]);
	cache_destroy(G->cfg->node_cache[NODE_SAM]);

	cache_destroy(cfg->edge_cache);
	cache_destroy(cfg->frag_cache);

	cache_destroy(G->cfg->event_cache[EVENT_COAL]);
	cache_destroy(G->cfg->event_cache[EVENT_MIGR]);

	free(G);
#ifdef DEBUG
	fprintf(stderr, "Finishing function %s\n\n", __func__);
#endif
}


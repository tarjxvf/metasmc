void edge_set_init(struct edge_set *set, int maxn)
{
	set->maxn = maxn;
	set->n = 0;
	set->edges = malloc(sizeof(struct edge *) * maxn);
	memset(set->edges, 0, sizeof(struct edge *) * maxn);
}

void edge_set_clear(struct edge_set *set)
{
	set->n = 0;
	memset(set->edges, 0, sizeof(struct edge *) * set->maxn);
}

void edge_set_destroy(struct edge_set *set)
{
	set->maxn = set->n = 0;
	free(set->edges);
}

void edge_set_add(struct edge_set *set, struct edge *e)
{
	e->trunk_id = set->n;
	set->edges[set->n++] = e;
}

struct edge *edge_set_get(struct edge_set *set, int i)
{
	return set->edges[i];
}

struct edge *edge_set_remove(struct edge_set *set, int i)
{
	struct edge *e;

	e = set->edges[i];
	set->edges[i] = set->edges[--(set->n)];
	set->edges[i]->trunk_id = i;
	set->edges[set->n] = NULL;

	return e;
}

void
print_tree_structure (struct rb_node *node, int level)
{
  int i;

  /* You can set the maximum level as high as you like.
     Most of the time, you'll want to debug code using small trees,
     so that a large |level| indicates a ``loop'', which is a bug. */
  if (node == NULL)
    {
      fprintf (stderr, "<nil>");
      return;
    }

//  fprintf (stderr, "%d(", ((struct edge *)node->rb_data)->eid);
  fprintf (stderr, "%(", ((struct edge *)node->rb_data));

  for (i = 0; i <= 1; i++)
    {
      if (node->rb_link[i])
        {
            print_tree_structure (node->rb_link[i], level + 1);
        }
      else
        fprintf (stderr, "NULL");

      if (i == 0)
        fputs (", ", stderr);
    }

  fprintf(stderr, ")");
}

